def sum_of_three(num):
    '''
    sum_of_three(num) produces the sum of three integers, num, 27, and
      42.
    sum_of_three: Int -> Int

    Examples:
      sum_of_three(-2) => 67
      sum_of_three(0) => 69
      sum_of_three(1) => 70
    '''
    return num + 27 + 42
